package PBO.pertemuan3;

import java.util.Scanner;

public class lat1{

public static void main(String[] args){
    int x=7;

    x += 5; //tambah
    System.out.println("setelah x += 5 = " + x);

    x -= 5; //kurang
    System.out.println("setelah x -= 5 = " + x);

    x *= 5; //kali
    System.out.println("setelah x *= 5 = " + x);

    x /= 5; //bagi
    System.out.println("setelah x /= 5 = " + x);

    x %= 5; //sisa bagi
    System.out.println("setelah x %= 5 = " + x);
}
}
